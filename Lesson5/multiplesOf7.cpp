/*
Задача 3: Միջակայքի 7֊ի բազմապատիկներ
Պահանջվում է գրել ծրագիր, որը մուտքում կստանա A և B ամբողջ թվերը և 
կարտածի [A, B] միջակայքի բոլոր այն թվերի քանակը, 
որոնք առանց մնացորդի բաժանվում են 7֊ի։ (դաս 5, 6)
*/
#include <iostream>
int main () 
{
    int A, B, count;
    std::cin >> A >> B;
    count = 0;
    while (A <= B)
    {
        if(A % 7 == 0)
        {
            ++count;
        }
        ++A;
    }
    std::cout << count;
}