/*
Задача 20: Հակադիր թվեր
Գրել ծրագիր, որը մուտքում կստանա A, B, C և D ամբողջ թվերը։ 
Պահանջվում է արտածել 
YES եթե A-ն հակադիր է D֊ին և B֊ն հակադիր չէ C ֊ին, 
NO հակառակ դեպքում։ (դաս 4)
*/
#include <iostream>
int main () 
{
    int A, B, C, D;
    std::cin >> A >> B >> C >> D;
    if (A == -D && B != -C)
    {
        std::cout << "YES";
    }
    else
    {
        std::cout << "NO";
    }
    
}