/*Задача 18: Չաճող հաջորդականություն
Գրել ծրագիր, որը մուտքում կստանա 4 ամբողջ թիվ և կարտածի YES, 
եթե մուտքագրված թվերը կազմում են չաճող հաջորդականություն և 
NO հակառակ դեպքում։ (դաս 4)
*/
#include <iostream>
int main () 
{
	int a, b, c, d;
	std::cin >> a >> b >> c >> d;
	if ((d-c) <=0 && (c-b)<=0 && (b-a)<=0)
	{
	std::cout << "YES";
	}
	else
	{
	std::cout << "NO";
	}
}